                	<h2>Frequently Asked Questions</h2>
		<p>If you are looking to find out more information about Syracuse University Ambulance, this page features some of the most common questions we receive. As always, if our website is unable to provide you with the answers you are looking for, feel free to contact us. </p>
		
		<hr />
		
		<h4 style="margin-bottom:-15px;">When is SUA in service?</h4>
		<p>SUA operates 24 hours a day, 7 days a week whenever classes are in session.</p>
		<hr />
		
		<h4 style="margin-bottom:-15px;">How do I get in touch with SUA during an emergency?</h4>
		<p>In an emergency, calling <strong>315-443-4299</strong> will immediately connect you to a trained emergency dispatcher who can send an ambulance to your location and provide you with further instruction within seconds. Additionally, calling 711 from an on-campus phone, #SU from a cell phone or 315-443-2224 will connect you to the Department of Public Safety which can dispatch officers and connect you directly to SUA for medical instruction.</p>
		<hr />
		
		<h4 style="margin-bottom:-15px;">I want to join SUA! What do I do to get started?</h4>
		<p>SUA hires new members each semester. To start, check back on our website at the beginning of the semester for more information on the entire application process. First, you'll need to complete an online application before being invited back for a group and/or individual interview where we get to know a little more about you. From that pool, we will select 10-20 new members to enter our probationary program. If you apply and are not accepted due to number of applicants, we encourage you to apply again in the future. </p>
		<hr />		
		<h4 style="margin-bottom:-15px;">Does it cost anything to use SUA?</h4>
		<p>The simple answer is no. As a student at Syracuse University, you are required to pay a student health fee. Part of that fee covers the cost of emergency ambulance transport and medical transport from the hospital if you should need it. In some critical situations, advanced interventions are required to help ensure your health. In this situation, you may receive a bill from Rural/Metro Medical Services for the cost of having a paramedic assist you. </p>
                    <p>In the event that SUA is unavailable, the next closest available mutual aid ambulance will be dispatched. In this situation, you may recieve a bill from the transporting ambulance.</p>
		<hr />		
		
		<h4 style="margin-bottom:-15px;">Where will the ambulance take me if I get hurt or become sick?</h4>
		<p>Being so close to the city of Syracuse has its advantages. SUA is able to transport patients from SU to a number of area hospitals. They include Crouse Irving Memorial Hospital, SUNY Upstate Medical Center, St. Joseph's Hospital, Community General Hospital and in some cases, the Veteran's Association Medical Hospital. Additionally, SUA will transport to Syracuse University's Health Services center during business hours even if you do not have an appointment. Your EMT will explain your options and ensure that you are transported to the hospital that can best meet your needs. </p>
		<hr />  
		
		<h4 style="margin-bottom:-15px;">Do I have to be on campus to call SUA?</h4>
		<p>No. SUA's coverage area includes all University property but will also respond off campus to the Euclid neighborhood, into the city of Syracuse or anywhere else that students, faculty and staff may be at any time. </p>
		<hr />		
		
		<h4 style="margin-bottom:-15px;">Does SUA provide the same kind of ambulance care that other services do?</h4>
		<p>While operating as a student-run organization, SUA is held to the same strict standards of care and training that any other ambulance service is held to. Our membership completes both state, federal and in-house training almost constantly to remain a compliant service provider. We train all of our members but the personnel directly responsible for your care are all New York State Emergency Medical Technicians. </p>
		<hr />	
		
		<h4 style="margin-bottom:-15px;">I'm not that sick or don't want to call the ambulance, what can I do?</h4>
		<p>Requesting an ambulance does not automatically mean you will be transported to a hospital. In some cases, an evaluation from an EMT can put your mind at ease and help you to determine your illness/injury is not emergency enough to require immediate transport. Additionally, you can seek evaluation from Syracuse University Health Services if you do not wish to go to a hospital. When in doubt, it is always safer to call and have the EMT crew provide you with the best options for your situation. </p>
		<hr />		
		
		<h4 style="margin-bottom:-15px;">I know members of SUA and I'm afraid I'll be embarrassed if they come during an emergency. What should I do?</h4>
		<p>SUA, like all ambulance services and healthcare providers are held to the strictest patient confidentiality standards and guidelines. If you or someone else requires an ambulance, we will provide professional and compassionate care making sure we do not violate the privacy of any patients, bystanders or members. </p>
		<hr />		 
		 
		<h4 style="margin-bottom:-15px;">What is advanced life support?</h4>
		<p>SUA operates as a Advanced EMT agency. This allows us to provide you with a standard level of patient care that can include bleeding and wound management, airway maintenance, cardiac defibrillation, medication administration, splinting, oxygen therapy and more. In some situations, a higher level of care may be needed to treat you or someone else that is injured. In these situations, we will request a paramedic from Rural/Metro Medical Services to meet us on the scene and provide an extra set of hands to assist with IV fluid therapy, a wider array of medications, cardiac monitoring and more. These skills are considered advanced life support, or ALS.  </p>
