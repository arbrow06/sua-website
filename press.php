	<h4 style="margin-top:-15px;">SU News <span style="color:#7f7f7f;"> January 8, 2012</span></h4>	
	<p>SUA provides 24/7 ambulance services to faculty, staff and students at the university. The organization is almost entirely run by student members who are living a double life.There are 75 students on campus that are committed Syracuse University Ambulance members, responding to emergency and medical calls daily. The student members cover overnight shifts, evening shifts or 24-hour shifts as they try to move up in position, gain more responsibility and learn more and more about people they serve.</p>
	
